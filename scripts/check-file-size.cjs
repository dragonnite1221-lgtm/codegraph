#!/usr/bin/env node
"use strict";

// Enforce the CodeGraph 200-line TypeScript file limit.
//
// Existing violations are frozen in scripts/file-size-baseline.txt; no new
// violation, baseline growth, or stale baseline entry is allowed. Regenerate
// the baseline with `node scripts/check-file-size.cjs --write-baseline`.

const fs = require("fs");
const path = require("path");

const LIMIT = 200;
const ROOTS = ["src", "__tests__"];
const BASELINE = path.join("scripts", "file-size-baseline.txt");

// Generated/vendored declarations are not hand-maintained source.
function isExcluded(relPath) {
  return relPath.endsWith(".d.ts");
}

function collectTsFiles(dir, acc) {
  if (!fs.existsSync(dir)) return acc;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      collectTsFiles(full, acc);
    } else if (entry.name.endsWith(".ts")) {
      acc.push(full);
    }
  }
  return acc;
}

function lineCount(file) {
  const data = fs.readFileSync(file, "utf8");
  // Match `wc -l` semantics: count newline characters.
  let n = 0;
  for (let i = 0; i < data.length; i++) {
    if (data.charCodeAt(i) === 10) n++;
  }
  return n;
}

function collectViolations() {
  const violations = {};
  for (const root of ROOTS) {
    for (const file of collectTsFiles(root, [])) {
      const rel = file.split(path.sep).join("/");
      if (isExcluded(rel)) continue;
      const count = lineCount(file);
      if (count > LIMIT) violations[rel] = count;
    }
  }
  return Object.fromEntries(Object.entries(violations).sort());
}

function readBaseline() {
  const baseline = {};
  if (!fs.existsSync(BASELINE)) return baseline;
  const lines = fs.readFileSync(BASELINE, "utf8").split("\n");
  lines.forEach((raw, i) => {
    const line = raw.trim();
    if (!line || line.startsWith("#")) return;
    const match = line.match(/^(\d+)\s+(.+)$/);
    if (!match) {
      throw new Error(`${BASELINE}:${i + 1}: expected '<line_count> <path>'`);
    }
    const count = Number(match[1]);
    const rel = match[2];
    if (count <= LIMIT) {
      throw new Error(`${BASELINE}:${i + 1}: baseline counts must exceed ${LIMIT}`);
    }
    if (rel in baseline) {
      throw new Error(`${BASELINE}:${i + 1}: duplicate baseline path ${rel}`);
    }
    baseline[rel] = count;
  });
  return baseline;
}

function writeBaseline(violations) {
  fs.mkdirSync(path.dirname(BASELINE), { recursive: true });
  const rows = [
    "# Generated by scripts/check-file-size.cjs --write-baseline.",
    `# Format: <line_count> <path>; limit=${LIMIT}.`,
    "",
  ];
  for (const [name, count] of Object.entries(violations)) {
    rows.push(`${count} ${name}`);
  }
  fs.writeFileSync(BASELINE, rows.join("\n") + "\n");
}

function buildErrors(current, baseline) {
  const errors = [];
  for (const [p, count] of Object.entries(current)) {
    const allowed = baseline[p];
    if (allowed === undefined) {
      errors.push(`new violation: ${String(count).padStart(5)} ${p}`);
    } else if (count > allowed) {
      errors.push(`baseline grew: ${allowed} -> ${count} ${p}`);
    }
  }
  for (const [p, allowed] of Object.entries(baseline)) {
    if (current[p] === undefined) {
      errors.push(`stale baseline entry: was ${allowed} ${p}`);
    }
  }
  return errors;
}

function main() {
  const current = collectViolations();
  if (process.argv.includes("--write-baseline")) {
    writeBaseline(current);
    console.log(`Wrote ${Object.keys(current).length} baseline entries to ${BASELINE}.`);
    return 0;
  }
  let baseline;
  try {
    baseline = readBaseline();
  } catch (err) {
    console.error(err.message);
    return 2;
  }
  const errors = buildErrors(current, baseline);
  if (errors.length) {
    console.error(`TypeScript files over ${LIMIT} lines are blocked unless frozen in baseline.`);
    for (const e of errors) console.error(e);
    return 1;
  }
  console.log(`File size gate passed: ${Object.keys(current).length} baseline violations, no growth.`);
  return 0;
}

process.exit(main());
